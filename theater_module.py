
# Regular Price
def price(people):
    print("Regular Price for {0} is {1}".format(people, people * 10))

# Senior Price
def senior_price(people):
    print("Senior Price for {0} is {1}".format(people, people * 5))

# Children price
def children_price(people):
    print("Children Price for {0} is {1}".format(people, people * 7))
